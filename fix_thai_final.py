# Pattern: Thai char sequences that have \x80-\x9F control chars in them
# These represent 2-byte UTF-8 sequences where the second byte was a control char
# that cp874 maps to a non-Thai char

# Fix Thai chars mixed with control chars in the 0x80-0x9F range  
def fix_thai_with_controls(text):
    """
    Fix sequences where Thai Unicode is mixed with Latin/control chars
    that are actually cp874 encoded bytes.
    """
    result = []
    i = 0
    while i < len(text):
        c = text[i]
        # Check if this looks like start of a garbled sequence
        # Thai chars: U+0E00-U+0E7F
        # Control/special range after partial fix: U+0080-U+009F (from 0x80-0x9F bytes)
        if '\u0e00' <= c <= '\u0e7f' or '\u0080' <= c <= '\u009f':
            # Collect contiguous Thai+control chars
            j = i
            while j < len(text) and ('\u0e00' <= text[j] <= '\u0e7f' or '\u0080' <= text[j] <= '\u009f'):
                j += 1
            
            # Try to decode this sequence as cp874 bytes -> UTF-8
            chunk = text[i:j]
            bytes_out = bytearray()
            ok = True
            for ch in chunk:
                cp = ord(ch)
                if cp <= 0x9F:
                    # This char's codepoint IS the byte value (since cp874 maps 0x00-0x9F to same Unicode)
                    bytes_out.append(cp)
                elif '\u0e00' <= ch <= '\u0e7f':
                    # Thai char - encode back to cp874 byte
                    # Thai chars in cp874: byte = codepoint - 0x0E00 + 0xA0
                    byte_val = (ord(ch) - 0x0E00) + 0xA0
                    if 0xA0 <= byte_val <= 0xFF:
                        bytes_out.append(byte_val)
                    else:
                        ok = False
                        break
                else:
                    ok = False
                    break
            
            if ok and bytes_out:
                try:
                    fixed = bytes_out.decode('utf-8')
                    result.append(fixed)
                    i = j
                    continue
                except UnicodeDecodeError:
                    pass
            
            # Keep original char if can't fix
            result.append(c)
            i += 1
        else:
            result.append(c)
            i += 1
    
    return ''.join(result)

